package com.example.urbanmart.model;

public class User {
    private String id;
    private String email;
    private String name;
    private boolean isActive;

    // Getters
    public String getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    public String getName() {
        return name;
    }

    public boolean isActive() {
        return isActive;
    }
}
